class test:
    def __init__(self):
        self.val="hello world"
    def show(self):
        print(self.val)

obj=test()
obj.show()
#why we use constructor

#To initialize the object's attributes to their default values.

#To perform any necessary setup tasks, such as opening files or connecting to databases.

#To validate the data that is being passed to the constructor.

#To ensure that the object is in a valid state before it is used.